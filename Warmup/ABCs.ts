// Problem: ABCs
// Given three integers A, B, and C, determine their sum.

// Your task is to implement the function getSum(A, B, C) which returns the sum
// A + B + C.

// Constraints:
// 1 <= A, B, C <= 100

// Solution
// Time Complexity: O(1)
// Space Complexity: O(1)
// Explanation: None

export const getSum = (A: number, B: number, C: number): number => {
  return A + B + C;
};
